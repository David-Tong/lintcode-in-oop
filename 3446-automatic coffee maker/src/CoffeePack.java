public class CoffeePack {

    private final int neededMilk;

    private final int neededSugar;

    public CoffeePack(int milk, int sugar) {
        this.neededMilk = milk;
        this.neededSugar = sugar;
    }

    public int getNeededMilk() {
        return neededMilk;
    }

    public int getNeededSugar() {
        return neededSugar;
    }

}
